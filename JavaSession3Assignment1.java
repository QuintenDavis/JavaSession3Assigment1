
public class JavaSession3Assignment1 {

	public static void main(String[] args) {
	double cube = 125.0d;
	double sqaure = 100.0d;
	
	//
	// Get the cube root of double value
	//
	double cbrt = Math.cbrt(cube);
	System.out.println("Cube root of " + cube + " is " +  cbrt);
	
	//
	// Get the square root of double value
	//
	double sqrt = Math.sqrt(sqaure);
	System.out.println("Square root of " + sqaure + " is " + sqrt);

	}

}
